int main()
{
    int h, m;
    while (~scanf("%d:%d", &h, &m) && (h || m))
    {
        double a = fabs(h * 30 + m * 30.0 / 60 - m * 6);
        if (a > 180)
            a = 360 - a;
        printf("%.3lf\n", a);
    }
    return 0;
}