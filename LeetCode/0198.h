inline int bwmax(int l, int r) { return l ^ ((l ^ r) & -(l < r)); }

int rob(int *nums, int numsSize)
{
    int d = 0, p = 0; // DP
    while (numsSize--)
    {
        int tmp = d;
        d = bwmax(d, p + *nums++);
        p = tmp;
    }
    return d;
}
