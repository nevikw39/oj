inline int f(char x)
{
    switch (x)
    {
    case 'I':
        return 1;
    case 'V':
        return 5;
    case 'X':
        return 10;
    case 'L':
        return 50;
    case 'C':
        return 100;
    case 'D':
        return 500;
    case 'M':
        return 1000;
    }
    return 0;
}

int romanToInt(char *s)
{
    int y = 0;
    for (char *ptr = s; *ptr; ptr++)
        if (f(*ptr) >= f(1[ptr]))
            y += f(*ptr);
        else
            y -= f(*ptr);
    return y;
}
