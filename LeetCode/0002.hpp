class Solution
{
public:
    ListNode *addTwoNumbers(ListNode *l1, ListNode *l2)
    {
        ListNode *front = nullptr, *ptr;
        int carry = 0;
        while (l1 || l2)
        {
            ptr = (front ? ptr->next : front) = new ListNode((l1 ? l1->val : 0) + (l2 ? l2->val : 0) + carry);
            carry = ptr->val / 10;
            ptr->val %= 10;
            l1 = l1 ? l1->next : nullptr;
            l2 = l2 ? l2->next : nullptr;
        }
        if (carry)
            ptr->next = new ListNode(carry);
        return front;
    }
};
