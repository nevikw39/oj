struct edge
{
    int64_t u, v, w;
    constexpr bool operator<(const edge &x) const
    {
        return w < x.w;
    }
};

int main()
{
    nevikw39;
    int64_t n, r = 0;
    cin >> n;
    vector<edge> v(n - 1);
    for (edge &i : v)
    {
        cin >> i.u >> i.v >> i.w;
        --i.u;
        --i.v;
    }
    sort(ALL(v));
    dsu ds(n);
    for (const edge &i : v)
    {
        r += i.w * ds.size(i.u) * ds.size(i.v);
        ds.merge(i.u, i.v);
    }
    cout << r << '\n';
    return 0;
}