inline int bwmax(int l, int r) { return l ^ ((l ^ r) & -(l < r)); }

int main()
{
    int n, m, d = 0;
    scanf("%d%d", &n, &m);
    while (n--)
    {
        int a, b;
        scanf("%d%d", &a, &b);
        d = bwmax(d, (m - a + b - 1) / b);
    }
    printf("%d\n", d);
    return 0;
}
